<h2>Regisztrálás, bejelentkezés, profil</h2>
<p>
A program teljes funkcionalitását bejelentkezés után tudjuk használni.
</p>
<p>Lehetőségünk van a megszokott módon email cím és jelszó megadásával regisztrálni a programba, 
    vagy Google, Facebook fiokunk segitségével belépni.</p>
<p>Ha regisztrálunk akkor meg kell adnunk egy un "nick nevet" is, ezen a néven fog bennünket a programba
    többi felhasználója látni.</p>
<p>Bejelentkezés után a "profil" almenüben tudunk avatar képet feltölteni. Ha nem töltün fel 
    avatar képet akkor a program megkisérel a gravatar.com oldalról globális avatar képet letölteni az email címünk alapján.</p>
<p>Egyes esetekben (beállítás függő) a google és a facebook belépés is szolgáltat avatar képet.</p>         
<p>A profil oldalon van lehetőségünk a fiokunk törlésére is. A fiók tötrlése esetén a leadott szavazatok, küldött üzenetek,
    feltöltött fájlok és egyéb aktivitások "anonimizálásra kerülnek". Ez azt jelenti, hogy a továbbiakban nem lesz a programból megállapítható, hogy azok kihez kapcsolódtak.
</p>
