<h2>Videó/audió/szöveges chat kezelése okos telefonon</h2>
<div>Példa képpen a "Regisztrált felhasználók" csoport chat felületének használatát mutatjuk be.</div>
<div>A program fő menüjében koppintsunk a „Csoportok” -ra.</div>
<img src="/img/help/netpolgar-fomenu.jpg" />
<div>A megjelenő csoport listán koppintsunk a „- Regisztrált felhasználók” szövegre...</div>
<img src="/img/help/teams.jpg" />
<div>Így a csoport adatlapja jelenik meg. A bal oldali ikonokkal jelzett almenű
    a legfelső nyilra koppintva kinyitható...</div>
<img src="/img/help/team-adatlap.jpg" />
<div>A most megjelenő csoport adatlapon; a baloldali almenüben koppintsunk a “Beszélgetések” almenüre...</div>
<img src="/img/help/team-submenu.jpg" />
<div>A most megjelenő fórum képernyő alsó részén látható "kamera - mikrofon" ikonra koppintva indítjuk
az online chat alrendszert.</div>
<img src="/img/help/chat-start-button.jpg" />
<div>Sajnos ami most jön az operációs rendszerenként, típusonként, böngészőként más és
más :(. Gyakran előfordul, hogy engedélyezni kell a mikrofon és kamera használatát. 
Lehetséges, hogy ezt külön a böngésző beállításaiban lehet megtenni. 
Egyes esetekben az alkalmazás könyvtárból kell telepíteni a javasolt jitsi-meat alkalmazást. 
Sajnos ezt mindenkinek a saját rendszerén kell valahogy kitapasztalnia. Végül el kell jutni
oda, hogy az alábbi képen látható módon lásd saját magadat és a 
„Join meeting” gomb megjelenjen (természetesen mindenkinél a saját neve lesz fölé írva) 
Ekkor  erre a „Join meeting” gombra kell koppintani...</div>
<img src="/img/help/join.jpg" />
<div>Ezután a chat alap képernyője jelenik meg. 
Mindig az látszik aki éppen beszél. 
Az alsó ikonsoros menü előhívásához a képernyő alsó részére kell koppintani. </div>
<img src="/img/help/chat-alap.jpg" />
<div>Az alsó ikon menüben lévő mikrofon jelre kattintva elnémíthatjuk a mikrofonunkat, 
a kamera jelre kattintva kikapcsolhatjuk a kameránkat. 
Ismételt kattintással pedig vissza kapcsolhatjuk azokat. </div>
<img src="/img/help/chat-nemit.jpg" />
<div>A <em class="fas fa-comment-alt"></em> ikonnal írásos chat felület nyílik meg...</div>
<img src="/img/help/chat-msg-1.jpg" />
<div>Erről a chat -ről tudni kell, hogy az ide beírt dolgok csak addig őrződnek meg amíg az
online chat tart, amikor mindenki kilép akkor a beírt szövegek törlődnek. 
A bal oldali képernyő rész tetején lévő “X” -el az online szöveges chat képernyőt csukhatjuk be.</div>
<div>Az alsó menün lévő <em class="fas fa-user-friends"></em> ikonra kattintva az online felhasználók listáját 
láthatjuk...</div>
<img src="/img/help/chat-user-list.jpg" />
<div>Ez az online  felhasználó lista a  jobb felső “X” -el becsukható.</div>
<div>&nbsp;</div>
<div>Az alul lévő piros "telefonkagyló" gombbal lépünk ki a videó chat -ből.</div>
<img src="/img/help/chat-vege.jpg" />
<div>Ekkor az ingyenes jitsi rendszer reklám oldala jelenik meg, 
    innen a főmenüt használva léphetünk tovább.</div>
