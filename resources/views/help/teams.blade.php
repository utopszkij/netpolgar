<h2>Csoport szervezés</h2>
</p>
A netpolgár programban a hasonló érdeklődésű tagok vagy egy projekten közösen dolgozó tagok számára, 
hierarchikus „fa szerkezetű” csoport struktúra alakítható ki (fő csoport – csoport – alcsoport stb). 
Egy felhasználó több csoportban is tag lehet. 
A csoport tagok a program segítségével „chatelhetnek” egymással, videó konferenciákat bonyolíthatnak le, 
vitákat folytathatnak, szavazásokat rendezhetnek.
</p>
<p class="remark">
( Megjegyzés: a csoport szerkezet hierarchikus felépítése a csoportok közötti könnyebb eligazodást segíti, illetve később a likvid demokrácia kialakításánál lesz szerepe)
</p>
<p>
A felhasználók ”jelentkezhetnek” csoport tagnak. A teljes jogú tagsághoz az szükséges, 
hogy a csoport már meglévő tagjai közül – a csoport beállításainál megadott számú – tag támogassa az új 
tag belépését.
</p>
<p class="remark">
(Megjegyzés: ha a csoport létszám kisebb a beállított támogatási szükségletnél, akkor a tagok 100% -os támogatása szükséges. Nulla „szükséges támogatás” beállításánál a jelentkezők azonnal teljes jogú tagok lesznek.)
</p>
<p>
A csoport tagjai javasolhatják tag kizárását. A tag akkor lesz kizárva ha a csoport tagok – a csoport 
beállításainál %-ban megadott – hányada javasolta a kizárást.
</p>
<p>
A csoportokban alcsoportok hozhatóak létre. Alcsoport létrehozását a csoport tagok javasolhatják. 
A csoport akkor nyílik meg ha a javaslat a csoport tagoktól megkapja - a csoport beállításoknál 
megadott – szükséges támogatást. A javasolt csoportban chatelni, vitákat, szavazásokat szervezni, 
alcsoportokat szervezni csak a szükséges támogatás elérése után lehet.
</p>
<p class="remark">
(Megjegyzés: ha a csoport létszám kisebb a beállított támogatási szükségletnél, akkor a tagok 100% -os támogatása szükséges. Nulla „szükséges támogatás” beállításánál az alcsoport
azonnal megnyílik.)
</p>
<p>
A csoport tagjai javasolhatják a csoport lezárását. A csoport akkor lesz lezárva ha a tagok – a csoport beállításainál %-ban megadott – hányada javasolta a lezárást.
</p>
<p>
Minden csoportnak van legalább egy „adminisztrátora” alapértelmezetten a csoportot javasló felhasználó az adminisztrátor. Az adminisztrátor moderálhatja a csoportban lévő beszélgetéseket, módosíthat, javíthat a vita kérdések és a hozzájuk tartozó alternatívák szövegein, módosíthatja a csoport nevét, leírását és beállításait.
</p>
<p>
A csoport aktiválása után bármelyik csoport tag jelentkezhet adminisztrátornak. A tisztséget akkor töltheti be ha a csoport tagok – a csoport beállításnál %-ban megadott – hányada támogatja azt.
</p>
<p>
A csoport tagok kezdeményezhetik valamelyik adminisztrátortól az adminisztrálási jog megvonását. A jog akkor kerül megvonásra ha a csoport tagok – a beállításoknál %-ban megadott – hányada ezt javasolta.
</p>
<p>
A csoportokban egyéb tisztségek (pl. vita moderátor, képviselő, szóvivő stb) is lehetnek, ezek kezelése az adminisztrátori tisztséghez hasonló.
</p>
<p>
A legfelső szintű csoportokat a rendszer adminisztrátor kezeli. Van egy különleges működésű csoport a „Regisztrált felhasználók” ennek automatikusan tagja mindenki aki regisztrál a programba.
</p>
