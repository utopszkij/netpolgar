    <h2>Döntéshozatal</h2>
    <h3>Vita javaslat</h3>
    <p>Csoportokban és projektekben lehet a csoport illetve projekt tagjainak 
        eldöntendő kérdésekről vitát javasolni.</p>
    <p>Példaképpen csoportnál mutatjuk be a folyamatot. A csoport adatlapon kattintsunk az 
        "Eldöntendő kérdések" menüpontra.</p>
        <img src="/img/help/desktop-csoport-vita.png" />    
    <p> Ekkor a jelenleg meglévő viták és vita javaslatok 
        listája jelenik meg.</p>
    <p>A viták, javaslatok címére kattintva részletes infókat kapunk az adott kérdésről, 
        és ezen az oldalon lesz lehetőségünk mások javaslatait támogatni, a saját vita 
        javaslatunkhoz új megoldási  opciókat javasolni. Új vita javaslat tételéhez 
        kattintsunk az "Új vita indítását javaslom" gombra.</p>
    <img src="/img/help/desktop-vitak.png" />    
    <p>Új vita javaslatnál a képernyő kitöltése után a "Tárolás"-ra kattintunk.</p>
    <img src="/img/help/desktop-ujvita.png" />    
    <p>A vita listán a vita nevére kattintva nyílik meg a vita részletes adatképernyője.</p>
    <img src="/img/help/desktop-vita.png" />    
    <p>Itt a vita indító megoldási javaslatokat vihet fel az "Új opciót javaslok" gombra kattintva.</p>
    <p>A mások által indított vita javaslatoknál pedig a vita leírása alatt lévő 
    <em class="fas fa-thumps-up"></em> "like" ikonra kattintással támogathatjuk a vita megindítását. 
    Ismételt kattintással a támogatás vissza vonható.</p>
    <p>Ha a vita javaslat megkapta a létrehozásakor beállított szükséges támogatást, akkor automatikusan 
        "vita" állapotba kerül.</p>
    <p>A sajátmagunk által indított vita javaslatnál a <em class="fas fa-edit"></em> ikon segitségével 
        módosításra is lehetőség van.</em></p>
    <p>A csoport / projekt adminisztrátorok mások által indított vita javaslatokat is módosíthatnak.</p>    
