    <h2>Döntéshozatal</h2>
    <h3>Vita</h3>
    <p>Csoportokban és projektekben lehetnek viták.</p>
    <p>Példaképpen csoportnál mutatjuk be a folyamatot. A csoport adatlapon kattintsunk az 
        "Eldöntendő kérdések" menüpontra.</p>
    <img src="/img/help/desktop-csoport-vita.png" />    
    <p> Ekkor a jelenleg meglévő viták és vita javaslatok  listája jelenik meg.</p>
    <img src="/img/help/desktop-vitak.png" />    
    <p>A viták címére kattintva részletes infókat kapunk az adott kérdésről</p> 
    <img src="/img/help/desktop-vita2.png" />    
    <p>Itt a csoport / projekt tagjai megoldási javaslatokat vihetnek fel az 
        "Új opciót javaslok" gombra kattintva.</p>
    <p>A megoldási opcióknál a <em class="fas fa-thumbs-up"></em> ikonnal támogathatjuk 
    a megoldási opció felvételét a virtuális szavazó lapra. Ismételt kattintással a 
    támogatásunk vissza vonható.</p>
    <p>Ha egy opció javaslat elérte a megkívánt támogatást akkor "javasolt opció" -ból "opció" lesz, ezt 
        a továbbiakban már nem szükséges (nem is lehet) "like"-olni</p>
    <p>A "Hozzászólások" almenüt használva a vitához kapcsolodó fórum felületre kerülönk, 
        ahol videó / audió chat -be is beléphetünk.</p>    
    <p>A vita javaslat felvitelénél beállított idő eltelte után a vita automatikusan "szavazás" -á 
        alakul. A szavazáson azok az opciók fognak szerepeli amelyikek megkapták a 
        felvitelkor beállított támogatást.</p>
    <p>A sajátmagunk által indított vita javaslatnál a <em class="fas fa-edit"></em> ikon segitségével 
        módosításra is lehetőség van.</em></p>
    <p>A csoport / projekt adminisztrátorok mások által indított vitákat is módosíthatnak.</p>    
