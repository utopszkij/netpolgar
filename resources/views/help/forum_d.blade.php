<h2>Fórum kezelése asztali gépen</h2>
<div>Példa képpen a "Regisztrált felhasználók" csoport fórum felületének használatát mutatjuk be.</div>
<div>A program fő menüjében kattintsunk a „Csoportok” -ra.</div>
<div>A megjelenő csoport listán kattintsunk a „- Regisztrált felhasználók” szövegre...</div>
<img src="/img/help/desktop-teams.png" />
<div>A most megjelenő csoport adatlapon; a baloldali almenüben kattintsunk a “Beszélgetések” almenüre...</div>
<img src="/img/help/desktop-team.png" />
<p>Így a csoport „fórum” felületére jutunk. Itt üzeneteket írhatunk, olvashatjuk mások üzeneteit. 
A képernyőn azok az üzenetek láthatóak amiket a mostani oldal megnyítás előtt vittek fel. 
Ha valaki éppen most ír üzenetet az csak a következő ide látogatásunkkor, vagy a 
képernyő frissítése után lesz látható. 
Hasonlóan a mi beírásaink is csak azoknál látszanak akik ezután látogatnak ide vagy frissítik az oldalt. 
Ha sok üzenet van akkor a lista „lapozható”.</p>
<p>Lehetőségünk van egy meglévő üzenetre reagálni, 
vagy új üzenetet beírni. Ez utóbbi lehetőséghez a képernyő alján lévő beviteli dobozt használjuk. 
Az üzenet beírása után a „Küldés” -re
kattintunk ...</p>
<img src="/img/help/desktop-forum-uj.png" />
<p>Ha egy meglévő üzenetre akarunk válaszolni akkor a balra nyíllal és „Válasz” felirattal 
jelzett ikonra kattintunk.
Természetesen itt is a „Küldés” kattintással fejezzük
be az üzenet írást...</p>
