<h2>Videó/audió/szöveges chat kezelése asztali gépen</h2>
<div>Példa képpen a "Regisztrált felhasználók" csoport chat felületének használatát mutatjuk be.</div>
<div>A program fő menüjében kattintsunk a „Csoportok” -ra.</div>
<div>A megjelenő csoport listán kattintsunk a „- Regisztrált felhasználók” szövegre...</div>
<img src="/img/help/desktop-teams.png" />
<div>A most megjelenő csoport adatlapon; a baloldali almenüben kattintsunk a “Beszélgetések” almenüre...</div>
<img src="/img/help/desktop-team.png" />
<div>A most megjelenő fórum képernyő alsó részén látható "kamera - mikrofon" ikonra kattintva indítjuk
az online chat alrendszert.</div>
<img src="/img/help/desktop-chat-start.png" />
<div>Sajnos ami most jön az operációs rendszerenként, böngészőnként más és
más :(. Gyakran előfordul, hogy engedélyezni kell a mikrofon és kamera használatát. 
Lehetséges, hogy ezt külön a böngésző beállításaiban lehet megtenni. 
Sajnos ezt mindenkinek a saját rendszerén kell valahogy kitapasztalnia. Végül el kell jutni
oda, hogy az alábbi képen látható módon lásd saját magadat és a 
„Join meeting” gomb megjelenjen (természetesen mindenkinél a saját neve lesz fölé írva) 
Ekkor  erre a „Join meeting” gombra kell kattintani...</div>
<img src="/img/help/desktop-chat-join.png" />
<div>Ezután a chat alap képernyője jelenik meg. 
Mindig az látszik aki éppen beszél. 
Az alsó ikonsoros menü előhívásához a képernyő alsó részére kell vinni az egeret. </div>
<img src="/img/help/desktop-chat-alap.png" />
<div>Az alsó “négy kis négyzet” ikonnal megoszthatjuk a képernyőt két részre, ilyenkor
az összes résztvevőt láthatjuk a jobb oldalon kis bélyegképként. (Ebben a példában két résztvevő
van a “T” jelű résztvevőnek nincs kamerája.)</div>
<img src="/img/help/desktop-chat-mozaik.png" />
<div>Az alsó ikon menüben lévő mikrofon jelre kattintva elnémíthatjuk a mikrofonunkat, 
a kamera jelre kattintva kikapcsolhatjuk a kameránkat. 
Ismételt kattintással pedig vissza kapcsolhatjuk azokat. 
A <em class="fas fa-comment-alt"></em> ikonnal írásos chat felület nyílik meg...</div>
<img src="/img/help/desktop-chat-msg.png" />
<div>Erről a chat -ről tudni kell, hogy az ide beírt dolgok csak addig őrződnek meg amíg az
online chat tart, amikor mindenki kilép akkor a beírt szövegek törlődnek. 
A bal oldali képernyő rész tetején lévő “X” -el az online szöveges chat képernyőt csukhatjuk be.</div>
<div>Az alsó menün lévő <em class="fas fa-user-friends"></em> ikonra kattintva az online felhasználók listáját 
láthatjuk...</div>
<img src="/img/help/desktop-chat-users.png" />
<div>Ez az online  felhasználó lista a  jobb felső “X” -el becsukható.</div>
<div>&nbsp;</div>
<div>Az alul lévő piros "telefonkagyló" gombbal lépünk ki a videó chat -ből.</div>

