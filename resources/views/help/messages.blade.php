    <p>Válaszolhatunk a mások által írt üzenetekre. 
    Ha a web böngésző támogatja a mikrofon és kamera használatát akkor video-chat is lebonyolítható.</p>
    <h3>Privát üzenet küldése egy felhasználónak</h3>
    <p>
    A "csoportok" / "regisztrált felhasználók" / "tagok" menüpontjában megkeressük a felhasználót.
    A megjelenő üzenetküldő formba beirjuk az üzenetet vagy a "mikrofon" + "kamera" ikonra kattintva 
    videó chat-et indítunk
    <h3>Privát üzenet fogadása</h3>
    A bejelentkezés után a felső menű jobb oldalán lévő "privát üzenetek" menüpontban olvashatjuk a nekünk 
    küldött üzeneteket vagy csatlakozhatunk a privát videó-chat -hez. 
    <h3>Üzenet küldése csoportnak, projekt résztvevőknek, eseményre jelentkezőknek</h3>
    A csoportok / projekt / esemény menüjében lévő "beszélgetések" menüpontban megjelenő üzenetküldő 
    formba beirjuk az üzenetet vagy a "mikrofon" + "kamera" ikonra kattintva videó chat-et indítunk. 
    <h3>Csoport, projekt, esemény üzenet fogadása</h3>
    A csoportok / projekt / esemény menüjében lévő "beszélgetések" menüpontban olvashatjuk az üzeneteket 
    vagy csatlakozhatunk a videó-chat -hez.
