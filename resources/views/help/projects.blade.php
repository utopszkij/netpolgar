<h2>Projekt menedzselés</h2>
<p>
A rendszer adminisztrátorok, csoport adminisztrátorok projekteket indíthatnak. 
A projektek egy csoporthoz is kapcsolódhatnak. 
A projektek témakörökhöz rendelhetőek. 
Minden projektnek van legalább egy projekt adminisztrátora. 
Az adminisztrátorok körlevel küldhetnek a csoport tagjainak.
A regisztrált felhasználók résztvevőnek jelentkezhetnek a projektbe. 
A projekt tagok bármikor kiléphetnek a projektből. 
A projekt adminisztrátorok feladatokat (taszkokat) definiálhatnak a projektbe, határidőt, felelőst 
adhatnak meg a feladathoz, módosíthatják a feladat állapotát. 
A projekt adminisztrátorok lezárhatják, felfüggeszthetik a projektet. 
A projekt felvitelekor a felvivő lesz az adminisztrátor, később további adminisztrátorokat is kijelölhet 
a résztvevők közül. 
</p>
