    <h2>Döntéshozatal</h2>
    <h3>Szavazás</h3>
    <p>Csoportokban és projektekben lehetnek szavazások.</p>
    <p>Példaképpen csoportnál mutatjuk be a folyamatot. A csoport adatlapon kattintsunk az 
        "Szavazások" menüpontra.</p>
    <img src="/img/help/desktop-csoport-szavazas.png" />    
    <p> Ekkor a jelenleg folyamatban lévő szavazások listája jelenik meg.</p>
    <img src="/img/help/desktop-szavazasok.png" />    
    <p>A szavazások címére kattintva részletes infókat kapunk az adott kérdésről, 
        illetve - ha még nem szavaztunk - akkor szavazhatunk.</p> 
    <img src="/img/help/desktop-szavazas-most.png" />    
    <p>Itt a csoport / projekt tagjai - ha még nem szavaztak - a "Most szavazok" gombra kattintva 
        adhatják le szavazatukat.</p>
    <p>A szavazó képernyő a szavazás tipustól függően eltérő, ebben a példában egy "több X -es" szavazás 
        képernyőjeét láthatjuk.</p> 
    <img src="/img/help/desktop-szavazoform.png" />    
    <p>A szavazás közben az eddig leadott szavazatok alapján számolt  részeredmények is láthatóak.
        Ebben a részeredményben csak a ténylegesen leadott szavazatok vannak benne, a likvid 
        módszer szerint számított szavazat sulyozás még nincs elvégezve (az csak akkor fog megtörténni amikor 
        a szavazás lezárul és bmegállapítható ki nem adott le a kérdésben saját maga szavazatot).</p>
    <p>A vita inditáskor beállított szavazási időtartam végén a szavazás automatikusan lezárul, 
        ezután a végeredmény a csoport / projekt "Határozatok" almenüjében tekinthető meg</p>
    <img src="/img/help/desktop-szavazas-eredmeny.png" />    
    </p>    
